package net.reminitous.civilizationsmod.civilization;

/**
 * Enum representing the five types of civilization classes.
 * Determines player perks, NPC behavior, and class-specific mechanics.
 */
public enum CivilizationClass {
    AGRICULTURAL, // Farmers: increased crop yield, faster breeding, faster woodcutting
    WARLIKE,      // Warriors: increased combat damage, knockback weapons, ranged bonuses
    TECHNOLOGY,   // Engineers: redstone use, faster smelting and mining, automation perks
    MYSTIC,       // Wizards: potion brewing, portal creation, enchantment bonuses
    MERCHANT      // Merchants: enhanced trading, resource bonuses, economy perks
}
